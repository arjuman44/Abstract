package typeCasting;

public class TypeCasting {
	
	public static void main(String[] args) {
		
	
	int x=5;
	
	double y = x;
	
	System.out.println(x+" "+y);
	
	
	double a =8.67;
	
	int b = (int)a;
	
	System.out.println(a+" "+b);
	
	char i = 'B';
	char j = i;
	
	System.out.println(i+" "+j);
	
	int p = 67;
	char q = (char)p;
	
	System.out.println(p+" "+q);
	
	
	}	

}
